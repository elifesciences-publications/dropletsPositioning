matDIR = {'Z:\analysis\Niv\rambam5 extract\symmetry breaking analysis\2017_12_21\mix1 sample2\',...
    'Z:\analysis\Niv\rambam5 extract\symmetry breaking analysis\2017_12_21\mix1 sample1\',...
